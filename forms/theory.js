// Forms : 
// Forms are used to collect user inputs. They can contain various types of fields such as text inputs, checkboxes, radio buttons, and dropdowns.
// Forms can be submitted to a server for processing or used to trigger client-side actions.

// Authentication :
// Authentication is the process of verifying the identity of a user or system. It ensures that users are who they claim to be, typically through credentials like usernames and passwords.

// Authorization :
// Authorization is the process of determining what actions or resources a user or system is allowed to access after they have been authenticated. It defines permissions and access levels.

// Verification :
// Verification is the process of confirming the accuracy or truthfulness of information. In the context of forms, it often involves checking that user inputs meet certain criteria or standards.


// Validation :
// Validation is the process of ensuring that user inputs are correct, complete, and meet specified requirements before being processed or stored. This can include checking for required fields, data formats, and value ranges.

// Captcha :
// Captcha (Completely Automated Public Turing test to tell Computers and Humans Apart) is a security measure used to differentiate between human users and automated bots. It often involves tasks that are easy for humans but difficult for machines, such as identifying distorted text or selecting images.